public class BaseClass {
    private int strength;
    private int agility;
    private int dexterity;
    private int constitution;
    private int wisdom;
    private int intelligence;
    private int charisma;

    #region Getters & Setters Attributes
    public int Strength {
        get { return strength; }
        set { strength = value; }
    }
    public int Agility {
        get { return agility; }
        set { agility = value; }
    }
    public int Dexterity {
        get { return dexterity; }
        set {  dexterity = value; }
    }
    public int Constitution {
        get { return constitution; }
        set { constitution = value; }
    }
    public int Wisdom {
        get { return wisdom; }
        set { wisdom = value;  }
    }
    public int Intelligence {
        get { return intelligence; }
        set { intelligence = value; }
    }
    public int Charisma {
        get { return charisma; }
        set { charisma = value; }
    }
    #endregion
}